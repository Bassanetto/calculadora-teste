package Operacoes;

/**
 * Classe que implementa a operação de subtração.
 */
public class Subtracao {
    /**
     * Realiza a operação de subtração entre dois números inteiros.
     *
     * @param valor1 O número do qual será subtraído o segundo número.
     * @param valor2 O número a ser subtraído de valor1.
     * @return O resultado da subtração entre valor1 e valor2.
     */
    public double Subtrair(double valor1, double valor2)
    {
        return valor1 - valor2;
    }
}
