package Operacoes;
/**
 * Classe que realiza a divisão
 */

public class Divisao {
    /**
     * Realiza a operação de divisão entre dois números inteiros.
     *
     * @param valor1 O número do qual será dividido do segundo número.
     * @param valor2 O número a ser dividido de valor1.
     * @return O resultado da divisão entre valor1 e valor2.
     */
    public double Dividir(double valor1, double valor2)
    {
        return valor1 / valor2;
    }
}
