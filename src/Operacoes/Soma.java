package Operacoes;
/**
 * Classe que realiza a soma
 */
public class Soma {
    /**
     * Realiza a operação de soma entre dois números inteiros.
     *
     * @param valor1 O número do qual será somado ao segundo número.
     * @param valor2 O número a ser somado de valor1.
     * @return O resultado da soma entre valor1 e valor2.
     */
    public double Somar(double valor1, double valor2)
    {
        return valor1 + valor2;
    }
}
