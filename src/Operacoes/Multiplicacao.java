package Operacoes;
/**
 * Classe que realiza a multiplicação
 */
public class Multiplicacao {
    /**
     * Realiza a operação de multiplicação entre dois números inteiros.
     *
     * @param valor1 O número do qual será multiplicado ao segundo número.
     * @param valor2 O número a ser multiplicado do valor1.
     * @return O resultado da multiplicação entre valor1 e valor2.
     */
    public double Multiplicar(double valor1, double valor2)
    {
        return valor1 * valor2;
    }
}
